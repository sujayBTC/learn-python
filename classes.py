class calculator:
    
    def add(self,num1,num2):
        print(num1+num2)

    def sub(self,num1,num2):
        print(num1-num2)

    def mul(self,num1,num2):
        print(num1*num2)

    def div(self,num1,num2):
        print(num1/num2)

math=calculator()

math.add(5,5)
math.sub(20,15)
math.mul(5,5)
math.div(10,2)